// print if a number is odd or even
public class c_Problem2 {
    public static void main(String[] args) {
        int number = 15;

        if (number % 2 == 0) {
            System.out.println("Number is even."); // executes if number is divisible by 2
        } else {
            System.out.println("Number is odd."); // executes if number is not divisible by 2
        }
    }
}
